WHERE display_name IS NOT NULL 
  AND display_name != 'Usuário' 
  AND display_name != ''
  AND student_name = 'Usuário';

-- Verificar quantos usuários foram atualizados
SELECT 
  COUNT(*) as total_usuarios_recuperados,
  COUNT(CASE WHEN phone IS NOT NULL THEN 1 END) as com_telefone,
  COUNT(CASE WHEN instagram IS NOT NULL THEN 1 END) as com_instagram,
  COUNT(CASE WHEN linkedin IS NOT NULL THEN 1 END) as com_linkedin,
  COUNT(CASE WHEN youtube IS NOT NULL THEN 1 END) as com_youtube,
  COUNT(CASE WHEN website IS NOT NULL THEN 1 END) as com_website,
  COUNT(CASE WHEN location IS NOT NULL THEN 1 END) as com_localizacao
FROM public.profiles 
WHERE student_name != 'Usuário' AND display_name != 'Usuário';

-- Verificar status dos user_dashboard completos
SELECT 
  COUNT(*) as total_dashboards,
  COUNT(CASE WHEN strategy_text IS NOT NULL AND strategy_text != '' THEN 1 END) as com_strategy_text,
  COUNT(CASE WHEN profile_highlights IS NOT NULL AND profile_highlights != '[]'::jsonb THEN 1 END) as com_profile_highlights,
  COUNT(CASE WHEN motivation_quote IS NOT NULL AND motivation_quote != '' THEN 1 END) as com_motivation_quote,
  COUNT(CASE WHEN instructions_text IS NOT NULL AND instructions_text != '' THEN 1 END) as com_instructions_text,
  COUNT(CASE WHEN 
    strategy_text IS NOT NULL AND strategy_text != '' AND
    profile_highlights IS NOT NULL AND profile_highlights != '[]'::jsonb AND
    motivation_quote IS NOT NULL AND motivation_quote != '' AND
    instructions_text IS NOT NULL AND instructions_text != ''
    THEN 1 END) as dashboards_completos
FROM public.user_dashboard;
